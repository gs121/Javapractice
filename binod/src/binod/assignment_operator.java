package binod;

public class assignment_operator {
	public static void main(String args[]) {
		int a = 5;
		a+=4;
		int b = 6;
		b = --b;
	     //b=b-16;
			System.out.println(a++);
			System.out.println(a);
			System.out.println(++a);
			
			
			System.out.println(b--);
			System.out.println(b);
			System.out.println(--b);
	}
	

}
